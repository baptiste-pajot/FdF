#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	a;

	i = -1;
	j = ft_strlen(str);
	while (++i < --j)
	{
		a = str[i];
		str[i] = str[j];
		str[j] = a;
	}
	return (str);
}
