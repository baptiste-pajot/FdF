#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	i;

	i = 0;
	if (s != NULL && ft_strlen(s) > 0 && f != NULL)
	{
		while (i < ft_strlen(s))
		{
			f(&s[i]);
			i++;
		}
	}
}
